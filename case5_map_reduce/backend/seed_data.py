def main():
    init_db()

    with engine.connect() as conn:
        # 清空既有文件
        conn.execute(documents.delete())
        conn.commit()

        # 寫入所有文件
        for doc in DOCUMENTS:
            conn.execute(insert(documents).values(**doc))
        conn.commit()

    print(f"✅ 成功寫入 {len(DOCUMENTS)} 份模擬公司報告")
    for doc in DOCUMENTS:
        print(f"   {doc['id']} [{doc['category']}] {doc['title']}")


